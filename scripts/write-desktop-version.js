const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const pkg = require(path.join(ROOT, "package.json"));

function writeDesktopVersion(outDir) {
  const manifest = {
    version: pkg.version,
    installer: "Discord-Remake-Setup.exe",
    portable: "Discord-Remake-Portable.exe",
    title: "Desktop update available",
    message: `Discord Remake ${pkg.version} is ready. Download the new installer to update the desktop app.`,
    publishedAt: new Date().toISOString().slice(0, 10),
  };

  fs.mkdirSync(outDir, { recursive: true });
  fs.writeFileSync(path.join(outDir, "desktop-version.json"), `${JSON.stringify(manifest, null, 2)}\n`);
  return manifest;
}

function writeAppVersionCs(outFile) {
  const content = `// Auto-generated by npm run build:desktop — do not edit manually.
namespace DiscordRemake;

internal static class AppVersion
{
    public const string Value = "${pkg.version}";
}
`;
  fs.writeFileSync(outFile, content);
}

module.exports = { writeDesktopVersion, writeAppVersionCs, getDesktopVersion: () => pkg.version };
